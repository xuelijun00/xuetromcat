public class TrackHttpError405Exception extends RuntimeException {

    /**
     * Constructs a new runtime exception with {@code null} as its
     * detail message.  The cause is not initialized, and may subsequently be
     * initialized by a call to {@link #initCause}.
     */
    public TrackHttpError405Exception(String baseMsg) {
        super("Method Not Allowed - 指定的资源不支持请求的方法。baseMsg :"+baseMsg);
    }
}
