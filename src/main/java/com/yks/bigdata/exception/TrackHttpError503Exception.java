public class TrackHttpError503Exception extends RuntimeException {

    /**
     * Constructs a new runtime exception with {@code null} as its
     * detail message.  The cause is not initialized, and may subsequently be
     * initialized by a call to {@link #initCause}.
     */
    public TrackHttpError503Exception(String baseMsg) {
        super("Service Unavailable - 服务是临时不可用 （例如预定的平台维护）。稍后再试。baseMsg :"+baseMsg);
    }
}
