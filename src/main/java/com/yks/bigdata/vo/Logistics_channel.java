public class Logistics_channel {
    String id;
    String short_code;
    String english_name;
    String chiness_name;
    String yks_channel_name;

    public Logistics_channel(String id,String short_code, String english_name, String chiness_name, String yks_channel_name){
        this.id = id;
        this.short_code=short_code;
        this.english_name=english_name;
        this.chiness_name=chiness_name;
        this.yks_channel_name=yks_channel_name;
    }

    public String getShort_code() {
        return short_code;
    }

    public void setShort_code(String short_code) {
        this.short_code = short_code;
    }

    public String getEnglish_name() {
        return english_name;
    }

    public void setEnglish_name(String english_name) {
        this.english_name = english_name;
    }

    public String getChiness_name() {
        return chiness_name;
    }

    public void setChiness_name(String chiness_name) {
        this.chiness_name = chiness_name;
    }

    public String getYks_channel_name() {
        return yks_channel_name;
    }

    public void setYks_channel_name(String yks_channel_name) {
        this.yks_channel_name = yks_channel_name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
